                                     <h3><strong>Course Description </strong></h3>
                            <p><span style="font-weight: 400;">The TQUK Level 2 Diploma in Business Administration is a short online course that gives students the flexibility to fast track their learning at their own pace and achieve a nationally recognised qualification. 
The course aims to develop the learners knowledge and understanding of working in business and administration - to fully prepare them to successfully enter into this career environment. In order to achieve this, students must complete a set of compulsory modules that will include teachings on a variety of administrative services, document production, information management and personal responsibilities at work. Through this, students will be able create long standing career pathways in a variety of business fields. 
As such, this qualification is ideal for anyone with an interest in the responsibilities and tasks involved in a business administration role. By exploring the principles of supporting events, project management and changes within a business environment,  students develop a competitive and impressive knowledge about this sector which can help them to  start a new career or progress within a current role.
Each student is allocated a personal tutor who will be there to guide and support them through the course. All units are internally then assessed by their personal tutor, through a range of different assignments, and at the end of the course, TQUK will carry out an External Quality Assurance of the qualifications. Learners will receive further support throughout the course from fellow students through our social forums, as well as excellent faculty and exemplary facilities. 

                            
                            
                            &nbsp;</span></p>

