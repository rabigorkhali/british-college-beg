  <h3 style="font-family: " mouse-500",="" arial,="" helvetica,="" sans-serif;"=""><strong>  Pearson HNC / HND in Business <span></span> </strong></h3>
                                <div class="full-detail">
                                    

                                   

                                    
                            <p><span style="font-weight: 400;">The Higher National Certificate (HNC) and Higher National Diploma (HND) in Business are designed to help you to secure the knowledge, skills and behaviour needed to be successful in the global business environment. These qualifications have been developed in close collaboration with experts from a wide range of professional bodies, businesses and universities.&nbsp; They represent the latest in professional standards and are highly valued by students who wish to go on to employment or progress to a final year at university.&nbsp;</span></p>
<p><span style="font-weight: 400;">The purpose of these qualifications is to support your professional development to enable you to meet the demands of employers in the business sector and adapt to a constantly changing world. These qualifications also widen access to higher education and enhance the career prospects of those who undertake them.&nbsp;</span></p>
<br><p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 200;">ENTRY REQUIREMENTS</span></h5><p></p>
<p><span style="font-weight: 400;">We will consider your application based on previous employment and qualifications. You would be expected to hold qualifications at level 3 or have relevant work-related experience. You should also have GCSE grades at A* to C (or equivalent and/or 9 to 4 (or equivalent) in subjects such as maths and English.</span></p>
<p><span style="font-weight: 400;">If English is not your first language you will also require an IELTS score at 5.5 or above.</span></p>
<p><span style="font-weight: 400;">All applicants must be at least 18 years old.</span></p>
<p><span style="font-weight: 400;">You will be required to attend an interview to discuss the course requirements and suitability.&nbsp;</span></p>
<br><p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 200;">&nbsp;COURSE CONTENT</span></h5><p></p>
<p><span style="font-weight: 400;">Higher Nationals provide a wide-ranging study of the business sector.</span></p>
<p><span style="font-weight: 400;">The </span><strong>Level 4 Higher National Certificate in Business</strong><span style="font-weight: 200;"> provides a broad introduction to the subject area. You will study a mandatory core of modules and select two optional units across a range of occupational sectors, preparing you for subject specialism at Level 5.</span></p>
<br>
<p><span style="font-weight: 400;">Core units for </span><strong>Level 4 Higher National Certificate in Business</strong><span style="font-weight: 400;">:</span></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Business and the Business Environment&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Marketing Essentials&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Human Resource Management&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Management and Operations&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Management Accounting&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Managing a Successful Business Project (Pearson-set unit).</span></li>
</ul>
<p><strong><strong>&nbsp;</strong></strong></p>
<p><span style="font-weight: 400;">Optional units include:</span></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Business Law</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Innovation and Commercialisation</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Entrepreneurship and Small Business Management&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Financial Accounting</span></li>
</ul><br>
<p><span style="font-weight: 400;">The </span><strong>Level 5 Higher National Diploma in Business</strong><span style="font-weight: 400;"> offers you seven different specialist pathways to support progression into relevant occupational areas or onto degree-level study. There is also a non-specialist ‘General Business’ pathway that allows you to complete the HND in Business without committing to a particular professional specialism.&nbsp;</span></p>
<p><span style="font-weight: 400;">Specialist pathways for </span><strong>Level 5 Higher National Diploma in Business</strong><span style="font-weight: 400;">:</span></p><br>

<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Accounting and Finance&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Business Management&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Entrepreneurship and Small Business Management</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Human Resource Management&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Marketing&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Operations Management&nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Law</span></li>
</ul>
<br>
<p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 200;">START DATES</span></h5><p></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">January / April / September</span></li>
</ul><br>
<p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 200;">HOW THE COURSE IS DELIVERED</span></h5><p></p>
<p><span style="font-weight: 400;">Each module is delivered face-to-face in a classroom setting at our campus in Woolwich, south-east London. You will receive a mix of class lectures, seminars, presentations, and group projects. You will be expected to undertake independent research in addition to the classes.&nbsp;</span></p>
<br>
<p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 400;">HOW THE COURSE IS ASSESSED</span></h5><p></p>
<p><span style="font-weight: 400;">Your work will be assessed in a range of different ways. You will complete coursework such as essays, reports and presentations, as well as examinations. The grades from formal assessment activities will count towards your grade for the module. Assessments are completed throughout the programme, ensuring that the workload is manageable.&nbsp;</span></p>
<br>
<p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 400;">WHAT WILL YOU ACHIEVE:</span></h5><p></p>

<p><span style="font-weight: 400;">On successful completion of the Higher National Certificate, you will receive the </span><strong>Pearson BTEC Level 4 Higher National Certificate in Business</strong></p>
<p><span style="font-weight: 400;">On successful completion of the Higher National Diploma, you will receive the </span><strong>Pearson BTEC Level 5 Higher National Diploma in Business </strong><span style="font-weight: 400;">with specialist pathways included within brackets in the qualification title:</span></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Accounting and Finance)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Business Management)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Entrepreneurship and Small Business Management)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Human Resource Management)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Marketing)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Operations Management)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">&#9632; &nbsp;Pearson BTEC Level 5 Higher National Diploma in Business (Law)</span></li>
</ul><br>
<p></p><h5 style="background: #17376e; padding: 15px; color: #fff; margin-bottom: 15px; text-underline-position: none;"><span style="font-weight: 400;">LENGTH OF THE COURSE</span></h5><p></p>
<p><strong>Level 4 Higher National Certificate in Business </strong><span style="font-weight: 400;">– 1-year full time</span></p>
<p><strong>Level 5 Higher National Diploma in Business </strong><span style="font-weight: 400;">– 2 years full time</span></p>
