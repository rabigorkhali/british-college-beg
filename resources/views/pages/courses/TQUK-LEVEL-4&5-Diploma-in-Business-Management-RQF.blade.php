                                     <h3><strong>Course Description </strong></h3>
                            <p><span style="font-weight: 400;">This split-level diploma in Business Management, is a one-and-a-half-year qualification that is designed for those who wish to progress straight to the final year of an Undergraduate Business Degree. Alternatively, this is a great option for those who wish to develop a career in the public or private business sector. 
 <br/> <br/>
 The first year covers a wide range of highly focused business and management modules that include managing communication, business organizations in a global context, and finance for managers. It will also develop the learners research skills as well as their personal development to prepare them for a career in the world of business.  
 <br/> <br/>
 The second year ‘Level 5 Diploma in Business Management’ qualification, further builds on these skills and develops the learners understanding of management in the context of general business. As such, it equips learners to find employment with junior management prospects in general business or alternatively continue with their studies in a related field. 
 <br/>  <br/>
 All units are internally assessed by a personal tutor, through a range of assessments that  include a portfolio of evidence -  and at the end of the course, TQUK will carry out an External Quality Assurance of the qualifications. Learners will receive further support throughout the course from fellow students through our social forums, as well as excellent faculty and exemplary facilities.  
